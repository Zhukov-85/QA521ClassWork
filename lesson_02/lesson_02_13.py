# ESCAPE последовательности

print("Hello World!\nHello python!")  # \n - newline перенос на новую строку
print()

print("Hello World!\tHello python!")  # \t - tab табуляция
print("Hello World!    Hello python!")
print()

print("Hello\rWorld! Hello Python!")  # \r - все что левее данной последовательности не выводится
print()

print("Hello World\b\b\b! Hello Python!")  # \b - backspace удаление символа слева от последовательности
print()

print("Hello World!\n\tHello python\n\t\tHello Guido\n\t\t\t")
